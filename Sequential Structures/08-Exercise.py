# Make a Program that asks how much you earn per hour and the number of hours worked in the month.
# Calculate and show the total of your salary in that month.

hourSalary = float(input("Enter your earnings per hour: "))

hoursWorked = float(input("Enter how much hours that you worked in the month: "))

salary = hourSalary * hoursWorked

print("Your salary this month equals ", salary)




